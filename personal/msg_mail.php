   $(".site-mailto[data-toggle='modal']").on("click", function(e){
			var target = $(this).attr('data-target');
			var url = $(this).attr('href');
			$(target).load(url,function(){ SearchUser() });
	});
	
	$("#cha-content div.chat-bady").click(function() {
                var attrib  = $(this).parent().find(".chat-list").attr('id'); 
                var $attrib = $("#"+attrib);
                $attrib.slideToggle(300);
                $(this).parent().toggleClass("closed");
				
				if(!$attrib.data("subMenuPopulated") && $attrib.is(":visible")){
                    $attrib.html("<div class='loader'>Loading...</div>");
                    $.ajax({  
                     cache: false,
                     url: "../library/chat_list.php",
                     dataType: "html",
                     //data: "&chatid="+attrib,
                     success: function (data) {   
                        $attrib.html(data).data("subMenuPopulated", true);
                      },
                      error: function(jqXHR, textStatus, errorThrown) {
                        //alert(textStatus);
                      }
                  });
               }  
      });

	
	
	function SearchUser(){
		$('#search_user').typeahead({
				source: function(q, process){
				return $.get('../data/lookuser.php', {q: q}, function(response){
					var data = new Array;
					for(var i in response) {
						data.push(response[i]['id'] +"#"+ response[i]['name'] +"#"+ response[i]['img'] +"#"+ response[i]['loc']);
					}
					return process(data);
				});
			  },
			  highlighter: function(item){
					var parts = item.split('#'),
					html = '<div class="typeahead user-typeahead">';
					html += '<div class="pull-left"><img src="../images/photo/'+parts[2]+'" width="32" height="32" class="img-rounded"></div>';
					html += '<div class="user-media">';
					html += '<div>'+parts[1]+'</div>';
					html += '<div>'+parts[3]+'</div>';
					html += '</div>';
					html += '<div class="clear"></div>';
					html += '</div>';
					return html;
				},
				updater: function(item) {
					var parts = item.split('#');
					    return parts[1];
				}
		   });
	}
})
