                      <h4 class="media-heading"><a class="question_link" target="_blank" href="#">联邦德国的三轴稳定炮塔试验车</a></h4>
                      <div class="media-detail">1960年代，西德开始尝试研发新型炮稳设备，不仅提供水平与垂直方向上的稳定，更能修正车体的侧倾角。最初提出的是一个三段式的摇摆炮塔方案，三层分别对应垂直、侧倾、水平方向的火炮稳定。不过该方案显然可行性较差，并没有获得通过。后来设计人员又提出了其他方案，不过都遭到了否决。1974年，德国人造出了一辆三轴稳定炮塔试验车（Dreiachsig stabilisierter Turm T III F）。这辆样车目前可找到的照片并不多，唯一一辆实车保存在德国科布伦茨的博物馆中。该车采取了半球状炮塔造型，以此来达到在水平、垂直、侧倾方向上的稳定，炮塔下半部呈半球状，炮塔上半部的正面构造则保持了一定避弹外形，炮塔后方为了配平火炮重量，似乎有安装作为配重的尾舱。
为了容纳炮塔下半球与驱动装置，试验车在豹1底盘基础上加高了车身部分。值得注意的是，该车的发动机升级为1000马力，豹1原本配备819马力的发动机，而由于车重仅40吨，故豹1的动力相当充沛，如果不是改装后大幅增重，想来也不至于更换发动机吧？这里假设该试验车的单位功率比接近豹1的水准，那么可以推测其车重应该在50吨上下，增重的部分除了加高的车体、新的发动机和炮塔驱动装置之外，大概就来自那个半球型炮塔了。（个人估计其正面防护水平或与豹1相当）

